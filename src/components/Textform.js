import React ,{useState} from 'react'
import PropTypes from 'prop-types';
import "./Textform.css"



export default function Textform(props) {
  const [isclicked,setClicked]=useState(false)
  const handleClickInsideForm=()=>{
    if(!isclicked){
      setText("")
      setClicked(true)
    }
  }

  const [text,setText]=useState("enter text here");
  const [output,setOutput]=useState()

  const handleChange=(event)=>{
    setText(event.target.value)
  }

  const handleUpClick=()=>{
    setOutput(text.toUpperCase())
    props.triggerAlert('converted to uppercase')
  }
  const handleLowerClick=()=>{
    setOutput(text.toLowerCase())
    props.triggerAlert('Converted to lower case')
  }

  const handleClear=()=>{
    setText("")
    setOutput("")
    props.triggerAlert('Textbox is cleared')
  }

  const handleCopy = () => {
    const outputTextArea = document.getElementById('exampleFormControlTextarea2');
    outputTextArea.select();
    navigator.clipboard.writeText(outputTextArea.value)
    props.triggerAlert('Text is copied')
  };

  const countWords=(abc)=>{
    let count=1
    if (countChar(abc)===0)
    return 0
    for (let i=1;i<abc.length;i++){
      if(abc[i]!==' ' && abc[i-1]===' ')
      count++;
    }
    return count
  }
  const countChar=(abc)=>{
    let count=0
    for (let i=0;i<abc.length;i++){
      if (abc[i]!==' ')
      count++;
    }
    return count
  }

  return (
    <div style={{color: props.mode === 'dark' ? 'white' : 'black'}}>

    <div className="form-group" id="input" >
    <h1>{props.heading}</h1>
    <textarea className="form-control" value={text} onChange={handleChange} onClick={handleClickInsideForm}
    id="exampleFormControlTextarea1" rows="6" style={{backgroundColor: props.mode === 'dark' ? '#2d2929' : 'white',color:props.mode==='dark'?'white':'black'}}></textarea>
    <button type="submit" onClick={handleUpClick} className="btn btn-primary">Convert to Uppercase</button>
    <button type="button" onClick={handleLowerClick} className="btn btn-warning">to LowerCase</button>
    <button type="button" onClick={handleClear} className="btn btn-danger">clear Text</button>
  </div>

  <div className="form-group" id='output' >
    <h1>Transformed Text</h1>
    <textarea className="form-control" value={output} id="exampleFormControlTextarea2" rows="5" style={{backgroundColor: props.mode === 'dark' ? '#2d2929' : 'white',color:props.mode==='dark'?'white':'black'}}></textarea>
    <button type="button" onClick={handleCopy} className="btn btn-success">Copy Text</button>
    
  </div>
  

  <div className="preview ">
    <p><b>Preview</b> : {countChar(text)} Characters and {countWords(text)} Words</p>
  </div>
  </div>
  )
  } 
  
